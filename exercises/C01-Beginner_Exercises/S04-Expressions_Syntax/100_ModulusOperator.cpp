void ModulusOperator::runExercise()     // copy the exercise name & paste it into main.cpp
{
    // the % modulus operator gives you the remainder (if any) if you divide the number
    // to the left of the % by the number to the right of it.

    DrawText("Reference Exercise:", 50, 150, BLUE, 20);
    DrawText("no solution needed.", 50, 180, BLUE, 20);

    seeout << "1000%199 == " << 1000%199 << "\n";
    seeout << "1000%200 == " << 1000%200 << "\n";
    seeout << "1000%201 == " << 1000%201 << "\n";
    seeout << "4%5 == " <<   4%5 << "\n";
    seeout << "5%5 == " <<   5%5 << "\n";
    seeout << "6%5 == " <<   6%5 << "\n";
    seeout << "7%5 == " <<   7%5 << "\n";
    seeout << "8%5 == " <<   8%5 << "\n";
    seeout << "9%5 == " <<   9%5 << "\n";
    seeout << "10%5 == " << 10%5 << "\n";
    seeout << "11%5 == " << 11%5 << "\n";
    seeout << "12%5 == " << 12%5 << "\n";
    seeout << "13%5 == " << 13%5 << "\n";
    seeout << "14%5 == " << 14%5 << "\n";
    seeout << "15%5 == " << 15%5 << "\n";

}


